CREATE TABLE RECEIPT
(
	RECEIPTNO CHAR(5) PRIMARY KEY NOT NULL,
	CUSTOMERNAME VARCHAR(20) NOT NULL,
	RECEIPTDATE DATE NOT NULL
);

CREATE TABLE SINGER
(
	SINGERID CHAR(3) PRIMARY KEY NOT NULL,
	SINGERNAME VARCHAR(20) NOT NULL
);

CREATE TABLE ALBUM
(
	SINGERID CHAR(3) NOT NULL,
	ALBUMNO CHAR(3) PRIMARY KEY NOT NULL,
	ALBUMNAME VARCHAR(30) NOT NULL,
	RELEASEYEAR INT NOT NULL,
	PRICE NUMERIC(10,2) NOT NULL,
	STOCK NUMERIC(10) NOT NULL,
	CONSTRAINT FK_SINGER FOREIGN KEY(SINGERID)
	REFERENCES SINGER(SINGERID)
);

CREATE TABLE DETAILITEM
(
	DETAILID CHAR(5) PRIMARY KEY NOT NULL,
	ALBUMNO CHAR(3) NOT NULL,
	RECEIPTNO CHAR(5) NOT NULL,
	AMOUNT INT NOT NULL,
	TOTALPRICE NUMERIC(15,2) NOT NULL,
	CONSTRAINT FK_ALBUM FOREIGN KEY(ALBUMNO)
	REFERENCES ALBUM(ALBUMNO),
	CONSTRAINT FK_RECEIPT FOREIGN KEY(RECEIPTNO)
	REFERENCES RECEIPT(RECEIPTNO)
);

